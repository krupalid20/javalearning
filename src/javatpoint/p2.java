package javatpoint;

public class p2
{
	public static void main(String s[])
	{
		int i,j,k,m;
		for(i=1;i<=5;i++)
		{
			for(k=5;k>i;k--)
			{
				System.out.print(" ");
			}
			for(j=1;j<=i;j++)
			{
				System.out.print(j);
			}
			for(m=j-2;m>0;m--)
			{
				System.out.print(m);	
			}
			System.out.print("\n");
		}
	}
}
			
			

														
	
